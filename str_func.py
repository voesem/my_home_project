def upper_string(text):
    """
    Функция, которая принимает на вход строку
    и возвращает ее со всеми заглавными буквами
    """
    return text.upper()


def titled_string(text):
    """
    Функция, возвращающая заглавные первые
    буквы каждого слова в строке
    """
    return text.title()
